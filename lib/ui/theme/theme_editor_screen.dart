import 'package:flutter/material.dart';
import 'package:flutter_colorpicker/flutter_colorpicker.dart';
import 'package:wurp/logic/themes/theme_model.dart';

class ThemeEditorScreen extends StatefulWidget {
  final CustomThemeModel? existingTheme;

  const ThemeEditorScreen({super.key, this.existingTheme});

  @override
  State<ThemeEditorScreen> createState() => _ThemeEditorScreenState();
}

class _ThemeEditorScreenState extends State<ThemeEditorScreen> {
  late final TextEditingController _nameController;
  late CustomThemeColors _colors;

  @override
  void initState() {
    super.initState();
    final t = widget.existingTheme;
    _nameController = TextEditingController(text: t?.name ?? '');
    _colors = t?.colors ?? CustomThemeColors.fromPrimary(const Color(0xFF6C5443));
  }

  @override
  void dispose() {
    _nameController.dispose();
    super.dispose();
  }


  Future<void> _pickColor({
    required String label,
    required Color current,
    required ValueChanged<Color> onPicked,
  }) async {
    Color temp = current;
    final result = await showDialog<Color>(
      context: context,
      builder: (ctx) => AlertDialog(
        title: Text('choose $label'),
        contentPadding: const EdgeInsets.fromLTRB(16, 16, 16, 0),
        content: SizedBox(
          width: 320,
          child: SingleChildScrollView(
            child: ColorPicker(
              pickerColor: current,
              onColorChanged: (c) => temp = c,
              enableAlpha: false,
              labelTypes: const [ColorLabelType.hex, ColorLabelType.rgb],
              pickerAreaHeightPercent: 0.7,
            ),
          ),
        ),
        actions: [
          TextButton(onPressed: () => Navigator.pop(ctx), child: const Text('Cancel')),
          FilledButton(
            onPressed: () => Navigator.pop(ctx, temp),
            child: const Text('Apply'),
          ),
        ],
      ),
    );
    if (result != null) setState(() => onPicked(result));
  }
  
  Future<void> _showAutoGenerateDialog() async {
    Color seedPrimary   = _colors.primary;
    Color? seedSecondary;
    Color? seedTertiary;
    bool useDark        = _colors.isDark;
    bool useSecondary   = false;
    bool useTertiary    = false;

    await showDialog<void>(
      context: context,
      builder: (ctx) => StatefulBuilder(
        builder: (ctx, setLocal) {
          return AlertDialog(
            title: const Text('Auto-Generate Theme'),
            content: SingleChildScrollView(
              child: Column(
                mainAxisSize: MainAxisSize.min,
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  const Text(
                    'Choose 1-3 seed colors to generate a matching palette. Secondary and tertiary seeds are optional; if left empty, they will be automatically derived from the primary seed.',
                    style: TextStyle(fontSize: 13, color: Colors.grey),
                  ),
                  const SizedBox(height: 16),

                  _SeedRow(
                    label: 'Primary (Required)',
                    color: seedPrimary,
                    enabled: true,
                    onToggle: null,
                    onColorTap: () async {
                      Color temp = seedPrimary;
                      await showDialog<void>(
                        context: ctx,
                        builder: (c2) => AlertDialog(
                          title: const Text('Primary Seed'),
                          content: SizedBox(
                            width: 300,
                            child: SingleChildScrollView(
                              child: ColorPicker(
                                pickerColor: seedPrimary,
                                onColorChanged: (c) => temp = c,
                                enableAlpha: false,
                                labelTypes: const [ColorLabelType.hex],
                              ),
                            ),
                          ),
                          actions: [
                            FilledButton(
                              onPressed: () => Navigator.pop(c2),
                              child: const Text('OK'),
                            ),
                          ],
                        ),
                      );
                      setLocal(() => seedPrimary = temp);
                    },
                  ),
                  const SizedBox(height: 12),

                  _SeedRow(
                    label: 'Secondary (optional)',
                    color: seedSecondary ?? HSLColor.fromColor(seedPrimary).withHue((HSLColor.fromColor(seedPrimary).hue + 30) % 360).toColor(),
                    enabled: useSecondary,
                    onToggle: (v) => setLocal(() {
                      useSecondary  = v;
                      seedSecondary = v ? seedSecondary ?? _colors.secondary : null;
                    }),
                    onColorTap: !useSecondary ? null : () async {
                      Color temp = seedSecondary ?? _colors.secondary;
                      await showDialog<void>(
                        context: ctx,
                        builder: (c2) => AlertDialog(
                          title: const Text('Secondary Seed'),
                          content: SizedBox(
                            width: 300,
                            child: SingleChildScrollView(
                              child: ColorPicker(
                                pickerColor: temp,
                                onColorChanged: (c) => temp = c,
                                enableAlpha: false,
                                labelTypes: const [ColorLabelType.hex],
                              ),
                            ),
                          ),
                          actions: [
                            FilledButton(
                              onPressed: () => Navigator.pop(c2),
                              child: const Text('OK'),
                            ),
                          ],
                        ),
                      );
                      setLocal(() => seedSecondary = temp);
                    },
                  ),
                  const SizedBox(height: 12),

                  _SeedRow(
                    label: 'Tertiary (optional)',
                    color: seedTertiary ?? HSLColor.fromColor(seedPrimary).withHue((HSLColor.fromColor(seedPrimary).hue + 60) % 360).toColor(),
                    enabled: useTertiary,
                    onToggle: (v) => setLocal(() {
                      useTertiary  = v;
                      seedTertiary = v ? seedTertiary ?? _colors.tertiary : null;
                    }),
                    onColorTap: !useTertiary ? null : () async {
                      Color temp = seedTertiary ?? _colors.tertiary;
                      await showDialog<void>(
                        context: ctx,
                        builder: (c2) => AlertDialog(
                          title: const Text('Tertiary Seed'),
                          content: SizedBox(
                            width: 300,
                            child: SingleChildScrollView(
                              child: ColorPicker(
                                pickerColor: temp,
                                onColorChanged: (c) => temp = c,
                                enableAlpha: false,
                                labelTypes: const [ColorLabelType.hex],
                              ),
                            ),
                          ),
                          actions: [
                            FilledButton(
                              onPressed: () => Navigator.pop(c2),
                              child: const Text('OK'),
                            ),
                          ],
                        ),
                      );
                      setLocal(() => seedTertiary = temp);
                    },
                  ),
                  const SizedBox(height: 20),
                  const Divider(),
                  const SizedBox(height: 8),

                  Row(
                    children: [
                      const Icon(Icons.dark_mode_outlined, size: 20),
                      const SizedBox(width: 10),
                      const Expanded(child: Text('Dark Mode')),
                      Switch(
                        value: useDark,
                        onChanged: (v) => setLocal(() => useDark = v),
                      ),
                    ],
                  ),
                ],
              ),
            ),
            actions: [
              TextButton(
                onPressed: () => Navigator.pop(ctx),
                child: const Text('Cancel'),
              ),
              FilledButton.icon(
                icon: const Icon(Icons.auto_fix_high, size: 16),
                label: const Text('Generate'),
                onPressed: () {
                  setState(() {
                    _colors = CustomThemeColors.fromSeeds(
                      primary:   seedPrimary,
                      secondary: useSecondary ? seedSecondary : null,
                      tertiary:  useTertiary  ? seedTertiary  : null,
                      dark:      useDark,
                    );
                  });
                  Navigator.pop(ctx);
                },
              ),
            ],
          );
        },
      ),
    );
  }
  
  void _save() {
    final name = _nameController.text.trim();
    Navigator.pop(
      context,
      CustomThemeModel(
        id:        widget.existingTheme?.id ?? UniqueKey().toString(),
        name:      name.isEmpty ? 'Custom Theme' : name,
        colors:    _colors,
        createdBy: widget.existingTheme?.createdBy,
        likesCount: widget.existingTheme?.likesCount ?? 0,
        isPublic:  widget.existingTheme?.isPublic ?? false,
        createdAt: widget.existingTheme?.createdAt,
      ),
    );
  }
  
  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(widget.existingTheme == null ? 'Create theme' : 'Edit theme'),
        actions: [
          Tooltip(
            message: _colors.isDark ? 'Switch to Light mode' : 'Switch to Dark mode',
            child: IconButton(
              icon: Icon(_colors.isDark ? Icons.light_mode_outlined : Icons.dark_mode_outlined),
              onPressed: () => setState(() {
                _colors = CustomThemeColors.fromSeeds(
                  primary:   _colors.primary,
                  secondary: _colors.secondary,
                  tertiary:  _colors.tertiary,
                  dark:      !_colors.isDark,
                );
              }),
            ),
          ),
          TextButton.icon(
            onPressed: _showAutoGenerateDialog,
            icon: const Icon(Icons.auto_fix_high, size: 16),
            label: const Text('Auto'),
          ),
          const SizedBox(width: 4),
        ],
      ),
      body: Column(
        children: [
          _ThemePreview(
            colors: _colors,
            name:   _nameController.text.isEmpty ? 'Mein Theme' : _nameController.text,
          ),

          Expanded(
            child: ListView(
              padding: const EdgeInsets.fromLTRB(16, 8, 16, 100),
              children: [
                TextField(
                  controller: _nameController,
                  decoration: const InputDecoration(
                    labelText: 'Theme-Name',
                    border: OutlineInputBorder(),
                    prefixIcon: Icon(Icons.label_outline),
                  ),
                  onChanged: (_) => setState(() {}),
                ),
                const SizedBox(height: 4),

                AnimatedSwitcher(
                  duration: const Duration(milliseconds: 200),
                  child: _colors.isDark
                      ? Padding(
                    key: const ValueKey('dark'),
                    padding: const EdgeInsets.only(top: 8),
                    child: Chip(
                      avatar: const Icon(Icons.dark_mode, size: 16),
                      label: const Text('Dark Theme'),
                      backgroundColor: Colors.grey[850],
                      labelStyle: const TextStyle(color: Colors.white70),
                    ),
                  )
                      : const SizedBox.shrink(key: ValueKey('light')),
                ),

                const _SectionHeader('Brand-Colors'),
                _colorRow('Primary',     _colors.primary,     (c) => _colors = _colors.copyWith(primary: c)),
                _colorRow('On Primary',  _colors.onPrimary,   (c) => _colors = _colors.copyWith(onPrimary: c)),
                _colorRow('Secondary',   _colors.secondary,   (c) => _colors = _colors.copyWith(secondary: c)),
                _colorRow('On Secondary',_colors.onSecondary, (c) => _colors = _colors.copyWith(onSecondary: c)),
                _colorRow('Tertiary',    _colors.tertiary,    (c) => _colors = _colors.copyWith(tertiary: c)),
                _colorRow('On Tertiary', _colors.onTertiary,  (c) => _colors = _colors.copyWith(onTertiary: c)),

                const _SectionHeader('Surface-Colors'),
                _colorRow('Background',    _colors.background,   (c) => _colors = _colors.copyWith(background: c)),
                _colorRow('On Background', _colors.onBackground, (c) => _colors = _colors.copyWith(onBackground: c)),
                _colorRow('Surface',       _colors.surface,      (c) => _colors = _colors.copyWith(surface: c)),
                _colorRow('On Surface',    _colors.onSurface,    (c) => _colors = _colors.copyWith(onSurface: c)),

                const _SectionHeader('Status-Colors'),
                _colorRow('Error',    _colors.error,   (c) => _colors = _colors.copyWith(error: c)),
                _colorRow('On Error', _colors.onError, (c) => _colors = _colors.copyWith(onError: c)),
              ],
            ),
          ),
        ],
      ),
      floatingActionButton: FloatingActionButton.extended(
        onPressed: _save,
        icon: const Icon(Icons.save_rounded),
        label: const Text('Speichern'),
      ),
    );
  }

  Widget _colorRow(String label, Color color, void Function(Color) update) {
    return _ColorRow(
      label: label,
      color: color,
      onTap: () => _pickColor(
        label: label,
        current: color,
        onPicked: (c) => setState(() => update(c)),
      ),
    );
  }
}

